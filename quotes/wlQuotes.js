function makeQuoteObj(singleLineStrings){
    const linesArray = singleLineStrings.split("\n\n");
    // console.log('first split: ', linesArray);
    const nestedLineAuthor = linesArray.map(line => line.split(' - '))
    // console.log('second split: ', nestedLineAuthor);

    const removedEmpties = nestedLineAuthor.filter(arr => arr.length > 0)
    // console.log('third step: ', removedEmpties)
    const makeNewQuoteObj = removedEmpties.map(nestedArray => {
        const temp = (nestedArray.length > 1) ? `"${nestedArray[1]}"` : null;
        
        const quote = `{Author: ${temp}, Quote: "${nestedArray[0]}"}`
        return quote;
    })
    // console.log('last step: ', makeNewQuoteObj)
    const blah = makeNewQuoteObj.join(',\n')
    return blah
}
const arrToStr = arr => {
    let str = ""

    arr.forEach( obj => {
        str += obj + ",\n"
    })
    return str;
}


